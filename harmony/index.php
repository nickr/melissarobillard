      <h3>Harmony Family Services, Inc.</h3>


      <p>For over forty years, Harmony Family Services has provided social services in the Abilene area including residential care and assistance for at-risk children and support services for families in crisis.</p>
        <p>Harmony provides a caring environment for abused or neglected children in our twenty-four hour emergency shelter or in one of our loving foster homes.</p>
        <p>Runaway or homeless children find safe, temporary shelter at Harmony with counseling services for both the child and family.</p>

        <p>Harmony Family Services.  Providing care and support for children and families in need.</p>
